	<div class="fivecol project-writeup">
		<p class="cap">To Canada with Love was a bit of an unusual project. Described as a &ldquo;web documentary,&rdquo; its aim was to present a collection of short videos via an online storytelling platform. It was submitted, in a demo format, as a finalist in a public awareness campaign contest for Imagine Canada. If chosen, the site would then have been developed in full.</p>
		
		<span class="date url-link">&middot; <a href="http://tocanada.triggersandsparks.com">Demo Site</a> &middot; </span>
				
		<p>Keeping that in mind, I established a structure and flow for the website that would accommodate expansion. After an introductory video, a homepage loaded with three sample videos. A placeholder navigation bar at the top of the page would have expanded and animated, allowing the user to show only videos from certain regions or cities, so they&rsquo;d be able to easily browse through a large number of videos.</p>

	</div>

	<div class="sevencol">
		<a href="<?php echo $path; ?>/frontpage.jpg"><img src="<?php echo $path; ?>/frontpage.jpg" alt="Frontpage" /></a>
		
		<p>The site demanded animation in order to feel less static, and more like it was telling a story. However, it also needed to work on mobile devices&mdash;and to be responsive&mdash;making it a bit of a challenge. All the animation on the site is done with pure CSS3 transitions, which presented a bit of a learning curve for me. The end result, however, feels like a polished product, without resorting to Flash for its animated elements.</p>
